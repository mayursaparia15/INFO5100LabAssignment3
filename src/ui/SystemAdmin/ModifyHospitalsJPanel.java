    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblHospitals.getSelectedRow();
        
        if(selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Please select a row to View Employee Details");
            return;
        }
        
        DefaultTableModel  model = (DefaultTableModel) tblHospitals.getModel();
        Hospital selectedHospital = (Hospital) model.getValueAt(selectedRowIndex, 0);
        
        txtName.setText(selectedHospital.getHospitalName());
        txtId.setText(String.valueOf(selectedHospital.getHospitalID()));
        txtCity.setText(selectedHospital.getCity());
        txtPostal.setText(selectedHospital.getPostalCode());
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblHospitals.getSelectedRow();
        
        if(selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Please select a row to Delete Employee Details");
            return;
        }
        
        DefaultTableModel  model = (DefaultTableModel) tblHospitals.getModel();
        Hospital selectedHospital = (Hospital) model.getValueAt(selectedRowIndex, 0);
        hospitalDirectory.DeleteHospital(selectedHospital);
        JOptionPane.showMessageDialog(this, "Employee Deleted!!");
        populateTable();
        
        txtName.setText("");
        txtId.setText("");
        txtCity.setText("");
        txtPostal.setText("");
    }//GEN-LAST:event_btnDeleteActionPerformed
    
