    private void populateTable() {
    
        DefaultTableModel  model = (DefaultTableModel) tblHospitals.getModel();
        model.setRowCount(0);
        
        for(Hospital hd : hospitalDirectory.getHospitalDirectory()){
            
            Object[] row = new Object[3];
            row[0] = hd;
            row[1] = hd.getHospitalID();
            row[2] = hd.getCity();
            
            model.addRow(row);
        }
        
    }


